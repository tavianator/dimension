#include "dimension.h"
#include <stdlib.h> /* For malloc */

dmnsn_object *
dmnsn_new_object()
{
  dmnsn_object *object = malloc(sizeof(dmnsn_object));
  if (object) {
    object->trans = dmnsn_identity_matrix();
  }
  return object;
}

void
dmnsn_delete_object(dmnsn_object *object)
{
  free(object);
}
